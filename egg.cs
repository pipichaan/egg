using System;
using System.Threading;
//Метод IsAlive
//IsAlive — это свойство класса Thread, которое позволяет проверить, активен ли поток в данный момент.
//Оно возвращает true, если поток все еще выполняется, и false, если поток завершил выполнение.
//Это свойство полезно для определения состояния потока, что может быть важно, если вам нужно
//сделать какие-то действия только после того, как поток завершится.
//Метод Join
//Join() — это метод класса Thread, который приостанавливает выполнение текущего потока
//(например, основного потока программы) до тех пор, пока вызываемый поток не завершится.
//Вы можете также передать в Join время в миллисекундах для ограничения времени ожидания.
//Если поток не завершится в указанный интервал, выполнение продолжится.
//Это позволяет синхронизировать работу потоков, гарантируя, что один поток подождет
//завершения другого перед продолжением своего выполнения.
class Program
{
    static void Main(string[] args)
    {
        // Создаем переменные для потоков
        Thread chicken = new Thread(Chicken);
        Thread egg = new Thread(Egg);

        // Запускаем потоки
        chicken.Start();
        egg.Start();

        // Ждем завершения потоков
        chicken.Join();
        egg.Join();

        // Проверяем, какой поток завершился последним
        if (chicken.IsAlive)
        {
            Console.WriteLine("Яйцо");
        }
        else
        {
            Console.WriteLine("Курица");
        }
    }
    static void Chicken()
    {
        // Простой вывод слова "Курица"
        Console.WriteLine("Курица");
    }

    static void Egg()
    {
        // Простой вывод слова "Яйцо"
        Console.WriteLine("Яйцо");
    }
}

  

